package sigevi.bea;

public class Usuario {
    private Integer codUsu;
    private String logUsu;
    private String nomUsu;
    private String apeUsu;
    private String pasUsu;
    private Integer perfil_codPer;
    private String nomPer;

    public Integer getCodUsu() {
        return codUsu;
    }

    public void setCodUsu(Integer codUsu) {
        this.codUsu = codUsu;
    }

    public String getLogUsu() {
        return logUsu;
    }

    public void setLogUsu(String logUsu) {
        this.logUsu = logUsu;
    }

    public String getNomUsu() {
        return nomUsu;
    }

    public void setNomUsu(String nomUsu) {
        this.nomUsu = nomUsu;
    }

    public String getApeUsu() {
        return apeUsu;
    }

    public void setApeUsu(String apeUsu) {
        this.apeUsu = apeUsu;
    }

    public String getPasUsu() {
        return pasUsu;
    }

    public void setPasUsu(String pasUsu) {
        this.pasUsu = pasUsu;
    }

    public Integer getPerfil_codPer() {
        return perfil_codPer;
    }

    public void setPerfil_codPer(Integer perfil_codPer) {
        this.perfil_codPer = perfil_codPer;
    }

    public String getNomPer() {
        return nomPer;
    }

    public void setNomPer(String nomPer) {
        this.nomPer = nomPer;
    }

}