package sigevi.bea;

public class Medida {
    private Integer codMed;
    private String nomMed;
    private String simMed;
    private int ancMed;
    private int larMed;

    public Integer getCodMed() {
        return codMed;
    }

    public void setCodMed(Integer codMed) {
        this.codMed = codMed;
    }

    public String getNomMed() {
        return nomMed;
    }

    public void setNomMed(String nomMed) {
        this.nomMed = nomMed;
    }

    public String getSimMed() {
        return simMed;
    }

    public void setSimMed(String simMed) {
        this.simMed = simMed;
    }

    public int getAncMed() {
        return ancMed;
    }

    public void setAncMed(int ancMed) {
        this.ancMed = ancMed;
    }

    public int getLarMed() {
        return larMed;
    }

    public void setLarMed(int lagMed) {
        this.larMed = lagMed;
    }

}