package sigevi.bea;

public class Reporte {

    private String cli;
    private String tip;
    private float mon;

    public String getCli() {
        return cli;
    }

    public void setCli(String cli) {
        this.cli = cli;
    }

    public String getTip() {
        return tip;
    }

    public void setTip(String tip) {
        this.tip = tip;
    }

    public float getMon() {
        return mon;
    }

    public void setMon(float mon) {
        this.mon = mon;
    }


}