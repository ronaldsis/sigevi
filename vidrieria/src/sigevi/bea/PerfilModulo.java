package sigevi.bea;

public class PerfilModulo {
    private Integer codPerMod;

    private Integer visible;

    private Integer modulo_codMod;

    private Integer perfil_codPer;

    public Integer getCodPerMod() {
        return codPerMod;
    }

    public void setCodPerMod(Integer codPerMod) {
        this.codPerMod = codPerMod;
    }

    public Integer getVisible() {
        return visible;
    }

    public void setVisible(Integer visible) {
        this.visible = visible;
    }

    public Integer getModulo_codMod() {
        return modulo_codMod;
    }

    public void setModulo_codMod(Integer modulo_codMod) {
        this.modulo_codMod = modulo_codMod;
    }

    public Integer getPerfil_codPer() {
        return perfil_codPer;
    }

    public void setPerfil_codPer(Integer perfil_codPer) {
        this.perfil_codPer = perfil_codPer;
    }
}